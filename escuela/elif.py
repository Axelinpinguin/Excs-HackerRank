numero=int(input('Ingrese el primer numero: '))
numero1=int(input('Ingrese  el segundo numero: '))
print()

if numero > numero1:
    print(f'el numero {numero} es mayor al numero {numero1}')
elif numero == numero1:
    print(f'el numero {numero} es igual a {numero1}')
else:
    print(f'el numero  {numero} es menor al {numero1}')

